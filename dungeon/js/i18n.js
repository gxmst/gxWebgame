export function localizeAffix(affix, language = "zh-CN") {
  if (!affix || normalizeLanguage(language) !== "en-US") return affix;
  return { ...affix, name: AFFIX_EN[affix.id] ?? AFFIX_EN[affix.stat] ?? affix.name };
}

export function localizeEffect(effect, language = "zh-CN") {
  if (!effect || normalizeLanguage(language) !== "en-US") return effect;
  const row = EFFECT_EN[effect.id] ?? EFFECT_EN[effect.type];
  return row ? { ...effect, name: row[0], description: row[1] } : effect;
}

export function localizeMaterial(material, language = "zh-CN") {
  if (!material || normalizeLanguage(language) !== "en-US") return material;
  const row = MATERIAL_EN[material.id];
  return row ? { ...material, name: row[0], description: row[1] } : { ...material, name: "Unidentified Material" };
}

export function getItemDisplayName(item, language = "zh-CN") {
  const stored = String(item?.name || "");
  if (normalizeLanguage(language) !== "en-US") return stored || "未命名装备";
  let prefix = "";
  let base = stored;
  for (const [zh, en] of Object.entries(ITEM_PREFIX_EN)) {
    if (base.startsWith(zh)) { prefix = en; base = base.slice(zh.length); break; }
  }
  if (ITEM_BASE_EN[base]) return `${prefix}${ITEM_BASE_EN[base]}`;
  const baseEntry = Object.entries(ITEM_BASE_EN).find(([zh]) => base.endsWith(zh));
  if (baseEntry) {
    const affix = localizeAffix(item?.affixes?.[0], language);
    return `${prefix}${affix?.name ? `${affix.name} ` : ""}${baseEntry[1]}`.trim();
  }
  const effect = localizeEffect(item?.effect, language);
  return `${prefix}${effect?.name ? `${effect.name} ` : ""}${getSlotLabel(item?.slot, language)}`.trim();
}

export function getUnitDisplayName(unit, language = "zh-CN") {
  const name = String(unit?.name ?? unit ?? "");
  if (normalizeLanguage(language) !== "en-US") return name;
  if (ENEMY_EN[name]) return ENEMY_EN[name];
  for (const [zh, en] of Object.entries(ENEMY_EN)) {
    if (name.includes(zh)) return name.replace(zh, en).replace("精英", "Elite");
  }
  return name;
}

export function localizeBattleLog(entry, language = "zh-CN") {
  if (!entry || normalizeLanguage(language) !== "en-US") return entry?.message ?? entry?.text ?? String(entry ?? "");
  const actor = getUnitDisplayName(entry.actor, language) || "Combatant";
  const targets = Array.isArray(entry.targets) ? entry.targets : [];
  const target = getUnitDisplayName(targets[0], language) || "target";
  const skill = CONTENT_EN.skills[entry.skill?.id] ?? ({ enemy_attack: "Attack", venom_fangs: "Venom Fangs", shadow_bolt: "Shadow Bolt", boss_smash: "Crushing Slam", boss_devour: "Devour", boss_guard: "Iron Guard", boss_flurry: "Savage Flurry" }[entry.skill?.id]) ?? entry.skill?.name ?? "Attack";
  if (entry.type === "battle_start") return `⚔️ Battle begins: ${actor} faces ${entry.targetIds?.length ?? 0} enemies.`;
  if (entry.type === "round_start") return `Round ${entry.round}`;
  if (entry.type === "defeat") return `💀 ${targets.map((unit) => getUnitDisplayName(unit, language)).join(", ") || target} fell.`;
  if (entry.type === "battle_end") return entry.outcome === "victory" ? "🏆 Victory!" : "☠️ Defeat.";
  if (entry.type === "reward") return `Gained ${entry.rewards?.experience ?? 0} XP and ${entry.rewards?.gold ?? 0} gold.`;
  if (entry.type === "round_limit") return "The round limit was reached. You retreat.";
  if (entry.type === "action") {
    if (entry.actionType === "heal") return `${actor} uses ${skill}, restoring ${entry.healing ?? entry.amount ?? 0} health.`;
    if (entry.actionType === "summon") return `${actor} uses ${skill} and summons reinforcements.`;
    if (entry.actionType === "guard") return `${actor} uses ${skill} and assumes a defensive stance.`;
    if (entry.actionType === "empower") return `${actor} uses ${skill} and enters an empowered form.`;
    if (entry.actionType === "aoe") return `${actor} uses ${skill}, hitting ${entry.targetIds?.length ?? targets.length} enemies for ${entry.damage ?? 0} damage.`;
    if (entry.dodged) return `${target} dodges ${actor}'s ${skill}.`;
    return `${actor} uses ${skill} on ${target}, dealing ${entry.damage ?? entry.amount ?? 0} damage${entry.critical ? " (Critical)" : ""}.`;
  }
  return String(entry.message ?? entry.text ?? "")
    .replace(/^第 (\d+) 回合$/, "Round $1")
    .replace("战斗胜利", "Victory").replace("战斗失败", "Defeat");
}

export function localizeDialogue(npc, node, language = "zh-CN") {
  if (!node || normalizeLanguage(language) !== "en-US") return node;
  const text = DIALOGUE_EN[`${npc?.id}:${node.id}`] ?? "The road ahead is dangerous. Stay prepared.";
  return { ...node, text, options: (node.options || []).map((option, index) => {
    const action = option.action || {};
    let label = `Choice ${index + 1}`;
    if (action.type === "acceptQuest") label = `❗ Accept: ${QUEST_EN[action.questId]?.[0] ?? "Quest"}`;
    else if (action.type === "turnInQuest") label = `✅ Complete: ${QUEST_EN[action.questId]?.[0] ?? "Quest"}`;
    else if (action.type === "end") label = "Farewell";
    else if (action.type === "goto") label = "Ask about this";
    return { ...option, label };
  }) };
}

