export function normalizeLanguage(language) {
  return language === "en-US" || language === "en" ? "en-US" : "zh-CN";
}

export function t(key, language = "zh-CN", vars = {}) {
  const locale = DICTIONARY[normalizeLanguage(language)] ?? DICTIONARY["zh-CN"];
  let value = locale[key] ?? DICTIONARY["zh-CN"][key] ?? key;
  return Object.entries(vars).reduce(
    (text, [name, replacement]) => text.replaceAll(`{${name}}`, String(replacement)),
    value,
  );
}

export function applyTranslations(root = document, language = "zh-CN") {
  const locale = normalizeLanguage(language);
  root.documentElement?.setAttribute("lang", locale);
  for (const element of root.querySelectorAll?.("[data-i18n]") ?? []) {
    element.textContent = t(element.dataset.i18n, locale);
  }
  for (const element of root.querySelectorAll?.("[data-i18n-aria]") ?? []) {
    element.setAttribute("aria-label", t(element.dataset.i18nAria, locale));
  }
  const title = root.querySelector?.("title");
  if (title) title.textContent = t("app.title", locale);
  return locale;
}

