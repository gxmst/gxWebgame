export function localizeContent(entry, type, language = "zh-CN") {
  if (!entry || normalizeLanguage(language) !== "en-US") return entry;
  if (type === "class") {
    const row = CONTENT_EN.classes[entry.id];
    return row ? { ...entry, name: row[0], role: row[1], archetype: row[1], description: row[2] } : entry;
  }
  if (type === "skill") {
    const name = CONTENT_EN.skills[entry.id];
    return {
      ...entry,
      name: name ?? entry.name,
      branches: Array.isArray(entry.branches) ? entry.branches.map((branch) => ({
        ...branch,
        name: CONTENT_EN.branches[branch.id] ?? branch.name,
        description: `Specialize ${name ?? entry.name} toward a distinct combat role.`,
      })) : entry.branches,
    };
  }
  if (type === "region") return { ...entry, name: CONTENT_EN.regions[entry.id] ?? entry.name, description: "A distinct adventure region with its own enemies, rewards, and progression checks." };
  if (type === "node") return { ...entry, name: CONTENT_EN.nodes[entry.id] ?? entry.name, description: "Explore this location for combat, supplies, and progression.", flavor: "The road ahead is dangerous, but the rewards grow with every step." };
  if (type === "event") {
    const row = EVENT_EN[entry.id];
    return row ? { ...entry, title: row[0], text: row[1], options: (entry.options || []).map((option, index) => ({ ...option, label: ["Take the risk", "Leave", "Investigate", "Help them"][index] ?? `Choice ${index + 1}` })) } : entry;
  }
  if (type === "quest") {
    const row = QUEST_EN[entry.id];
    return row ? { ...entry, name: row[0], description: row[1], progressText: String(entry.progressText || "").replace(/[^0-9/]+/g, "").trim() || entry.progressText } : entry;
  }
  if (type === "npc") {
    const row = NPC_EN[entry.id];
    return row ? { ...entry, name: row[0], blurb: row[1] } : entry;
  }
  return entry;
}

